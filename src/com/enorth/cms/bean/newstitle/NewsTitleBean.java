package com.enorth.cms.bean.newstitle;

public class NewsTitleBean {
	/**
	 * 当前新闻的类型
	 */
	private int newsType;
	/**
	 * 当前新闻的名字
	 */
	private int newsTitleName;

	public int getNewsType() {
		return newsType;
	}

	public void setNewsType(int newsType) {
		this.newsType = newsType;
	}

	public int getNewsTitleName() {
		return newsTitleName;
	}

	public void setNewsTitleName(int newsTitleName) {
		this.newsTitleName = newsTitleName;
	}
	
}
